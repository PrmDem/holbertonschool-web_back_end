#!/usr/bin/env python3
"""Concatenates two 'string' arguments
    and returns the resulting string

    Variables and expected return
    are validated via type annotation.
"""


def concat(str1: str, str2: str) -> str:
    """Args:
            str1 (str): initial string for concatenation
            str2 (str): string to concatenate to str1

        Returns:
            (str) The concatenated string
    """
    return str1 + str2
