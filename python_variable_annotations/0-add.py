#!/usr/bin/env python3
"""Adds two float-type numbers and returns the result.

    Variables and expected return
    are validated via type annotation.
"""


def add(a: float, b: float) -> float:
    """Args:
            a (float): first number to add
            b (float): second number to add

        Returns:
            (float) The sum of both floats
    """
    return a + b
